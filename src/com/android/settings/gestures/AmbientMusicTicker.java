package com.android.settings.gestures;

import android.os.Bundle;

import androidx.preference.Preference;

import com.android.internal.logging.nano.MetricsProto;
import com.android.settings.R;
import com.android.settings.SettingsPreferenceFragment;

import com.evolution.settings.preference.SystemSettingMasterSwitchPreference;

public class AmbientMusicTicker extends SettingsPreferenceFragment implements
        Preference.OnPreferenceChangeListener {

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        addPreferencesFromResource(R.xml.ambient_music_ticker);

        getActivity().getActionBar().setTitle(R.string.pulse_on_new_tracks_title);

        mFooterPreferenceMixin.createFooterPreference().setTitle(R.string.ambient_ticker_footer);
    }

    @Override
    public boolean onPreferenceChange(Preference preference, Object newValue) {
        return false;
    }

    @Override
    public int getMetricsCategory() {
        return MetricsProto.MetricsEvent.EVO_SETTINGS;
    }
}
